
def iseven(n):
    if n%2 ==0:
        return True
    else:
        return False


def isodd(n):
    if n % 2 != 0:
        return True
    else:
        return False


def add(a,b):
    return a+b


def mul(a,b):
    return a*b

def sub(a,b):
    return a-b

def divide(a,b):
    return a/b

def mod(a,b):
    return a%b

def ispositive(a):
    if a>0:
        return True
    else:
        return False


def isnegative(a):
    if a<0:
        return True
    else:
        return False